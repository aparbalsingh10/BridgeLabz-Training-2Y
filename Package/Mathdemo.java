import static java.lang.Math.*;
public class Mathdemo {
    public static void main(String[] args) {

        double x = -25;
        double a = 5;
        double b = 10;

        System.out.println("sqrt(25)        = " + sqrt(25));
        System.out.println("pow(2, 3)       = " + pow(2, 3));
        System.out.println("max(5, 10)      = " + max(a, b));
        System.out.println("min(5, 10)      = "+min(a, b));
        System.out.println("abs(-25)        = " + abs(x));
    }
}
